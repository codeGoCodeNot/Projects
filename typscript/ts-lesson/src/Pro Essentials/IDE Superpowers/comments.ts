/**
 * Adds two numbers together.
 * @param a - The first number.
 * @param b - The second number.
 * @returns The sum of a and b.
 */

const myFunc = (a: number, b: number) => a + b;
myFunc(1, 2);

/**
 *
 * @param x
 * @param y
 * @returns multiply x and y
 */

const anotherFunc = (x: number, y: number) => x * y;
anotherFunc(5, 5);
